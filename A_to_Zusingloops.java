// display
class A_to_Zusingloops
{
    char c;
    void compute_display()
    {
      for(c = 'A'; c <= 'Z'; c++)
      System.out.println(c + " ");
    }
    
 
    public static void main()
    {
      A_to_Zusingloops ob = new A_to_Zusingloops();
      ob.compute_display();
     
    }
}
        

